  ** 1) Conditionals ve Methods **
Algoritma:
1) Kullanıcıdan ilk sayı istenir.
2) Kullanıcıdan ikinci sayı istenir.
3) Kullanıcıdan yapılması istenen işlem istenir (+, -, *, /).
4) Kullanıcının yaptığı girişlere göre hesaplama işlemi yapılır.
5) Hesap sonucu ekrana yazdırılır.

İpucu:
Sayısal tipte bir değişkene herhangi bir sayısal değer atanabildiği gibi herhangi bir aritmetik işlem yapılarak bu işlemin sonucu da atanabilir.

Örnek 1:
double sayi = 1;
double toplam = 2 + 3;

Örnek 2:
double sayi1 = 4;
double sayi2 = 5;
double carpim = sayi1 * sayi2;

İyi çalışmalar. :)
    ** 2) Loops **
Palindrom:
Eğer bir kelimenin düz okunuşu ile ters okunuşu birbirinin aynı ise palindromdur, değilse palindrom değildir.
Örneğin 'ana' kelimesi bir palindrom iken 'baba' kelimesi bir palindrom değildir.
Basit bir palindrom kontrol konsol uygulaması yazınız:
1) Kullanıcı 'q' harfine basana kadar tekrar tekrar giriş yapabilir, 'q' harfine bastığında uygulamadan çıkılır.
2) Kullanıcının yaptığı giriş en az iki karakter olmalıdır.
3) Kullanıcı sadece harf girecek diye bir zorunluluk yoktur.
4) Eğer kullanıcının girdiği kelime palindrom ise ekrana 'Girdiğiniz kelime palindromdur.', değilse 'Girdiğiniz kelime palindrom değildir.' yazılır.
   ** 3) Loops **
Faktoriyel:
Bir sayının sürekli 1 azaltılarak arka arkaya yapılan çarpım faktoriyeldir.
Örneğin 5 faktoriyel 5! şeklinde gösterilir ve 5! = 5 * 4 * 3 * 2 * 1 olarak hesaplanır.
Basit bir faktoriyel hesaplama konsol uygulaması yazınız:
1) Kullanıcıdan tek seferlik pozitif bir tam sayı alınır.
2) Kullanıcının girdiği sayının faktoriyeli hesaplanarak ekrana yazdırılır.
  ** 4) Loops (Bonus) **
Yıldız ve boşluk karakterlerini kullanarak ekrana bir dama tahtası çizen basit bir konsol uygulaması.
Örnek:
*  *  *  *  
  *  *  *  *
*  *  *  *  
  *  *  *  *
*  *  *  *  
  *  *  *  *
*  *  *  *  
  *  *  *  *
  ** 5) DateTime (Bonus) ** 
Ekrandan kullanıcının doğum tarihini alan ve yaşını hesaplayan basit bir konsol uygulaması.
1 - Kullanıcıdan doğum tarihi istenir
2 - Yaş hesaplanır
3 - Ekrana yaş yazdırılır
